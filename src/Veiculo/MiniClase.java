package Veiculo;

/**
 *
 * @author josed
 */
public class MiniClase {
    String union;
    boolean hoja;

    public MiniClase(String union, boolean hoja) {
        this.union = union;
        this.hoja = hoja;
    }

    public String getUnion() {
        return union;
    }

    public void setUnion(String union) {
        this.union = union;
    }

    public boolean isHoja() {
        return hoja;
    }

    public void setHoja(boolean hoja) {
        this.hoja = hoja;
    }
    
    
}
